#include "3-calc.h"
/**
 * op_add - add two int
 * @a : first num
 * @b : second num
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract two int
 * @a : first num
 *  * @b : second num
 *   * Return: sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two int
 * @a : first num
 *  * @b : second num
 *   * Return: sum
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divided two int
 * @a : first num
 *  * @b : second num
 *   * Return: sum
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - moudles two int
 * @a : first num
 *  * @b : second num
 *   * Return: sum
 */
int op_mod(int a, int b)
{
	return (a % b);
}
